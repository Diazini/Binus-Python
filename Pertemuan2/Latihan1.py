panjang = int(input("Masukkan panjang balok (m): "))
lebar = int(input("Masukkan lebar balok (m): "))
tinggi = int(input("Masukkan tinggi balok (m): "))

volume = panjang * lebar * tinggi

print("Hasil Volume Balok Adalah")
print("Volume Kubik adalah ", volume, "meter³")

